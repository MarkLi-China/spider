public class Proxy {

    private String ip;

    private String port;

    private String anonymity;

    private String type;

    private String attribution;

    private String company;

    private String responseSpeed;

    private String lastTestTime;

    private String survivalTime;

    public String getIp() {

        return ip;
    }

    public void setIp(String ip) {

        this.ip = ip;
    }

    public String getPort() {

        return port;
    }

    public void setPort(String port) {

        this.port = port;
    }

    public String getAnonymity() {

        return anonymity;
    }

    public void setAnonymity(String anonymity) {

        this.anonymity = anonymity;
    }

    public String getType() {

        return type;
    }

    public void setType(String type) {

        this.type = type;
    }

    public String getAttribution() {

        return attribution;
    }

    public void setAttribution(String attribution) {

        this.attribution = attribution;
    }

    public String getCompany() {

        return company;
    }

    public void setCompany(String company) {

        this.company = company;
    }

    public String getResponseSpeed() {

        return responseSpeed;
    }

    public void setResponseSpeed(String responseSpeed) {

        this.responseSpeed = responseSpeed;
    }

    public String getLastTestTime() {

        return lastTestTime;
    }

    public void setLastTestTime(String lastTestTime) {

        this.lastTestTime = lastTestTime;
    }

    public String getSurvivalTime() {

        return survivalTime;
    }

    public void setSurvivalTime(String survivalTime) {

        this.survivalTime = survivalTime;
    }
}
